//OPERADORES 

let a = 3;
let b = 2;

console.log("SUMA DE UN NÚMEERO");
let z = a+ b;
console.log("Resultado de la suma: "+z); 

console.log("RESTA DE UN NÚMERO");
z = a -b;
console.log("El resultado de la resta: "+z);

console.log("MULTIPILICACIÓN DE UN NÚMERO");
z= a*b;
console.log("Resultadpo de la multiplicacion: "+z);

console.log("DIVISIÓN DE UN NÚMERO");
z= a /b;
console.log("Resultadpo de la división: "+z);

console.log("RESIDUO DE UNA OPERACIÓN;");
z = a % b;
console.log("Residuo de la operacion: "+z);

console.log("EXPONENTE DE UN NÚMERO");
z = a** b;
console.log("Resultado de operador exponente: "+ z);

console.log("INCREMENTO");
//PRE-INCREMENTO (EL OPERADOR ++ANTES DE LA VARIABLE)
z = ++a;
console.log(a);
console.log(z);

//POST-INCREMENTO ( EL OPERADOR ++ DESPUÉS DE LA VARIABLE)
z = b++;
console.log(b);
console.log(z);

console.log("DECREMENTO");
//PRE-DECREMENTO
z = --a;
console.log(a);
console.log(z);

// POST- DECREMENTO
z= b--;
console.log(b);
console.log(z);
